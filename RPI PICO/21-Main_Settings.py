from machine import UART
import time

# Set up serial communication
serial = UART(0,baudrate=9600)

Restore_Factory_Setting = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0x50, 0xAB, 0xCD])
Restore_user_Setting = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0x55, 0xAB, 0xCD])
Save_user_Setting = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0x56, 0xAB, 0xCD])
SAVE_TO_FLASH = bytes([0x7E, 0x00, 0x09, 0x01, 0x00, 0x00, 0x00, 0xDE, 0xC8])
light_sleep = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0xA0, 0xAB, 0xCD])
Deep_sleep = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0xA0, 0xAB, 0xCD])
wake_up = bytes([0x7E, 0x00, 0x08, 0x01, 0x00, 0xD9, 0xA0, 0xAB, 0xCD])

serial.write(Restore_Factory_Setting)
time.sleep(0.1)  # Add a delay between commands

# Read data from the Barcode Scanner Module:
while True:
    # Read data from the UART
    data = serial.read()

    # If there is data, print it to the console
    if data:
        print(data)

    time.sleep(1)
